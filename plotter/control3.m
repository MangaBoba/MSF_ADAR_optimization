function dYdt = control3(t, Y, P)
    % t  - текущий момент времени
    % Y - начальные значения переменных
    % P - параметры системы
       
    dy1 = P(1)*Y(2)*Y(3)-P(2)*Y(1);
    dy2 = P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3);
    dy3 = P(5)*Y(2)-P(6)*Y(3);
    
    psi2= Y(3)-P(9)*Y(2);

    u2=-psi2*(P(11).^-1)+P(9)*(P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3))-(P(5)*Y(2)-P(6)*Y(3));

    dYdt=[dy1;dy2;dy3+u2;psi2;u2];
end