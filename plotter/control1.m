function dYdt = control1(t, Y, P)%P(1-alpha,2-gam,3-mu,4-beta,5-del,6-lamb,7-0,,8-Y2const,9-ro1,10-ro1,11-T1,12-T2)

    psi1 = Y(1)-P(9)*Y(2);
    psi2 = Y(2)-P(8);%
    u1= P(2)*Y(1)-P(1)*Y(2)*Y(3)+P(9)*(P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3))-((Y(1)-P(9)*Y(2))/P(11));
    %f=(-psi2*(P(11).^-1)-P(3)*Y(2))/(P(3)-P(4)*Y(2)*(P(9).^-1));
    u2=-psi2*(P(12).^-1)-P(5)*Y(2)+P(6)*Y(3)+...
        ((-P(3)*(P(3)-P(4)*Y(2)*(P(9)))+(-psi2*(P(12).^-1)-P(3)*Y(2))*P(4)*(P(9)))/((P(3)-P(4)*Y(2)*(P(9))).^2))*...
        (P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3));
    
    
%     if(u1<50*75000-Y(2))
%         u1=0;
%     end
%     if(u1>Y(2)*0.01)
%         u1=dy2*0.01;
%     end
    
    
    dy1 = P(1)*Y(2)*Y(3)-P(2)*Y(1)+u1;
    dy2 = P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3);
    dy3 = P(5)*Y(2)-P(6)*Y(3)+u2;
    
   
    fi=(-(Y(2)-P(8))*(P(12).^-1)-P(3)*Y(2))/(P(3)-(P(4)*(P(9)*Y(2))));
    V=P(3)*(Y(2)+fi)-P(4)*P(9)*Y(2)*fi;
    
    dYdt=[dy1;dy2;dy3;psi1;psi2;u1;u2;V];%,psi1,psi2
end