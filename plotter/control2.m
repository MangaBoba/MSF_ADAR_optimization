function dYdt = control2(t, Y, P)
% t  - текущий момент времени
% Y - начальные значения переменных
% P - параметры системы

       psi1 = Y(1)-P(9)*Y(2);
       u1= P(2)*Y(1)-P(1)*Y(2)*Y(3)+P(9)*(P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3))-((Y(1)-P(9)*Y(2))/P(11));
         
    dy1 = P(1)*Y(2)*Y(3)-P(2)*Y(1)+u1;
    dy2 = P(3)*(Y(2)+Y(3))-P(4)*Y(1)*Y(3);
    dy3 = P(5)*Y(2)-P(6)*Y(3);
    dYdt=[dy1;dy2;dy3;psi1;u1];
end